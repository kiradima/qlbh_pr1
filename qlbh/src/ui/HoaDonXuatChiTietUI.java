    private void btnImportFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImportFileActionPerformed
        // TODO add your handling code here:
        JFileChooser jFileChooser = new JFileChooser();
        if (jFileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            File file = jFileChooser.getSelectedFile();
            String type = file.getName().substring(file.getName().lastIndexOf(".") + 1);
            if (type.equals("xls") || type.equals("xlsx")) {
                try {
                    ArrayList<HoaDonXuatChiTiet> list = ExcelHelper.importHoaDonXuatChiTiet(file);
                    int re = -1;
                    for (HoaDonXuatChiTiet o : list) {
                        re = HoaDonXuatChiTiet.insert(o);
                        if (re != 1) {
                            break;
                        }
                    }
                    if (re == 1) {
                        JOptionPane.showMessageDialog(null, "Thêm thành công");
                        refresh();
                    } else {
                        JOptionPane.showMessageDialog(null, "Thêm thất bại");
                    }
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Thêm thất bại");
                    Logger.getLogger(HoaDonXuatChiTietUI.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btnImportFileActionPerformed

    private void btnExportFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportFileActionPerformed
        // TODO add your handling code here:
        JFileChooser jFileChooser = new JFileChooser();
        if (jFileChooser.showSaveDialog(this) == JFileChooser.APPROVE_OPTION) {
            File file = jFileChooser.getSelectedFile();
            try {
                switch (export) {
                    // @export :
                    //      1 : tất cả
                    //      2 : thống kê
                    //      3 : tìm kiếm
                    case 1:
                        WordHelper.writeHDXCT(file, hoaDonXuatChiTiets,
                                "Thông tin hóa đơn xuất " + maHDX, maHDX);
                        break;
                    case 2:
                        WordHelper.writeTK(file, listTks, "Thống kê hóa đơn xuất "
                                + maHDX, thuocTinh);
                        break;
                    case 3:
                        WordHelper.writeHDXCT(file, hoaDonXuatChiTiets,
                                "Tìm kiếm trong hóa đơn xuất " + maHDX, maHDX);
                        break;
                }
            } catch (IOException ex) {
                Logger.getLogger(HoaDonXuatChiTietUI.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null, "Xuất file thành công");
        }
    }//GEN-LAST:event_btnExportFileActionPerformed

